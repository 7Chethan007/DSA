/**
 * @param {Function} fn
 * @param {any[]} args
 * @param {number} t
 * @return {Function} cancelFn
 */
var cancellable = function(fn, args, t) {
  // Call immediately at time 0
  fn(...args);

  // Schedule repeated calls every t ms
  const id = setInterval(() => {
    fn(...args);
  }, t);

  // Return the cancel function that will be invoked later (by setTimeout in tests)
  return function cancelFn() {
    clearInterval(id);
  };
};


